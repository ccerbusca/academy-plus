#include "libft.h"

int		ft_nrlen(int nr)
{
	int		len;

	if (nr == 0)
		return (1);
	len = 0;
	if (nr < 0)
		len = 1;
	while (nr > 0)
	{
		nr /= 10;
		len++;
	}
	return (len);
}
